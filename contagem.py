#O objetivo desse programa é  fazer uma contagem regressiva.
#Atribui o número 10 a variável "valor".
valor = 10

#Enquanto o valor for maior ou igual a zero.
while (valor >= 0):
    print(valor) #O numero vai vim para cá printando na tela.
    #O valor vai ser subtraido a menos um a cada passada.
    valor = valor - 1
#E no final printa essa mensagem. 
print("Contagem encerrada !")